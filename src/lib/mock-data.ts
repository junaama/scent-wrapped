// Parse description into tags
export function parseOutfitTags(description: string): string[] {
  return description
    .split(",")
    .map((tag) => tag.trim().toLowerCase())
    .filter((tag) => tag.length > 2);
}

// Get outfits with parsed tags
export function getOutfitsWithTags() {
  return MOCK_RUNWAY_OUTFITS.map((outfit) => ({
    ...outfit,
    tags: parseOutfitTags(outfit.description),
  }));
}
